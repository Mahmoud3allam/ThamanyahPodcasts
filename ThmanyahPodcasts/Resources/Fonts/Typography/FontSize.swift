import Foundation
public enum FontSize: CGFloat {
    case thin = 8.0
    case tabBar = 11.0 // The font size for tab bar items.
    case caption = 13.0 // The font size for captions or small labels.
    case body = 15.0 // The font size for general body text.
    case header5 = 20 // The font size for heading level 5.
    case header4 = 22.0 // The font size for heading level 4.
    case header3 = 25.0 // The font size for heading level 3.
    case header2 = 30.0 // The font size for heading level 2.
    case header1 = 35.0 // The font size for heading level 1.
}
